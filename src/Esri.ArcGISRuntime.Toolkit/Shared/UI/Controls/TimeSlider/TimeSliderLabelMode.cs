using System;
using System.Collections.Generic;
using System.Text;

namespace Esri.ArcGISRuntime.Toolkit.UI
{
    /// <summary>
    /// Represents the display mode for time slider labels
    /// </summary>
    public enum TimeSliderLabelMode
    {
        /// <summary>
        /// Display no labels along the time slider
        /// </summary>
        None,
        /// <summary>
        /// Display labels for the time slider's thumbs
        /// </summary>
        Thumbs,
        /// <summary>
        /// Display labels for the time slider's ticks
        /// </summary>
        Ticks
    }
}
